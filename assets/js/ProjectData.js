const cardsContainer = document.getElementById("cards-container");

const PREVIEW_CHARS = 120;


function escapeHtml(str) {
  return (str ?? '').toString()
    .replaceAll('&', '&amp;')
    .replaceAll('<', '<')
    .replaceAll('>', '>')
    .replaceAll('"', '"')
    .replaceAll("'", '&#39;');
}

function getPreview(desc) {
  const clean = (desc ?? '').toString().trim();
  if (clean.length <= PREVIEW_CHARS) return { preview: clean, full: clean };
  return {
    preview: clean.slice(0, PREVIEW_CHARS).trimEnd() + '…',
    full: clean,
  };
}

